func CreateAward(ctx *gin.Context) {
	user, err := models.GetUserFromContext(ctx)
	if err != nil {
		helpers.CreateResponse(ctx, helpers.Response{
			Message:    err.Error(),
			StatusCode: http.StatusInternalServerError,
			Data:       nil,
		})
		return
	}

	var req Request
	if err := ctx.ShouldBindJSON(&req); err != nil {
		helpers.CreateResponse(ctx, helpers.Response{
			Message:    err.Error(),
			StatusCode: http.StatusInternalServerError,
			Data:       nil,
		})
		return
	}

	newAward := models.Award{
		ProfileID:   user.Profile.ID,
		Title:       req.Title,
		Description: req.Description,
		Year:        req.Year,
	}

	resp, err := createAward(newAward, *user)
	if err != nil {
		helpers.CreateResponse(ctx, helpers.Response{
			Message:    err.Error(),
			StatusCode: http.StatusBadRequest,
			Data:       nil,
		})
	}

	helpers.CreateResponse(ctx, helpers.Response{
		Message:    "Successfully created award",
		StatusCode: http.StatusOK,
		Data:       resp,
	})
}

func GetAward(ctx *gin.Context) {
	var (
		year int
		err  error
	)
	if yearstr := ctx.Query("year"); yearstr != "" {
		if year, err = strconv.Atoi(yearstr); err != nil {
			helpers.CreateResponse(ctx, helpers.Response{
				Message:    err.Error(),
				StatusCode: http.StatusBadRequest,
				Data:       nil,
			})
			return
		}
		if year < 1900 || year > 2100 {
			helpers.CreateResponse(ctx, helpers.Response{
				Message:    "Year must be between 1900 and 2100",
				StatusCode: http.StatusBadRequest,
				Data:       nil,
			})
			return
		}
	}

	filter := Search{
		Title:       ctx.Query("title"),
		Description: ctx.Query("description"),
		Year:        year,
	}

	resp, total, page, perPage, err := getAward(filter, ctx.Query("page_size"), ctx.Query("page_number"))
	if err != nil {
		helpers.CreateResponse(ctx, helpers.Response{
			Message:    err.Error(),
			StatusCode: http.StatusInternalServerError,
			Data:       nil,
		})
		return
	}
	helpers.CreateResponse(ctx, helpers.Response{
		Message:    "successfully fetched awards",
		StatusCode: http.StatusOK,
		Data: map[string]interface{}{
			"data":     resp,
			"total":    total,
			"page":     page,
			"per_page": perPage,
		},
	})
}

